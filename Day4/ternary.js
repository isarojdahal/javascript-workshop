// Conditional/Ternary Operator
// Normal if-else
let isNepali = true;

if (isNepali) {
  console.log("Nepali honi");
} else {
  console.log("Haina");
}

// Ternary Operator | (condition) ? {if true} : {if false};
isNepali ? console.log("Nepali honi") : console.log("Haina");

console.log(isNepali ? "Nepali honi" : "haina");
